// Текущий язык (по умолчанию русский)
let currentLanguage = localStorage.getItem('language') || 'ru';

// Функция для установки языка
function setLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('language', lang);
  updateContent();
  updateActiveLanguageButton();
  // Оповещаем другие модули (пагинация, панель доступности) о смене языка
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

// Функция для получения перевода
function getTranslation(key) {
  return translations[currentLanguage]?.[key] || key;
}

// Функция для обновления содержимого страницы
function updateContent() {
  // Обновляем текстовые элементы
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (key && translations[currentLanguage]?.[key]) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translations[currentLanguage][key];
      } else {
        element.textContent = translations[currentLanguage][key];
      }
    }
  });

  // Обновляем атрибуты
  document.querySelectorAll('[data-i18n-attr]').forEach(element => {
    const [attr, key] = element.getAttribute('data-i18n-attr').split('|');
    if (attr && key && translations[currentLanguage]?.[key]) {
      element.setAttribute(attr, translations[currentLanguage][key]);
    }
  });

  // Обновляем плейсхолдеры
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    if (key && translations[currentLanguage]?.[key] && (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA')) {
      element.placeholder = translations[currentLanguage][key];
    }
  });

  // Обновляем опции select
  document.querySelectorAll('option[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (key && translations[currentLanguage]?.[key]) {
      element.textContent = translations[currentLanguage][key];
    }
  });

  // Обновляем атрибуты title
  document.querySelectorAll('[title]').forEach(element => {
    const title = element.getAttribute('title');
    if (title && translations[currentLanguage]?.[title]) {
      element.setAttribute('title', translations[currentLanguage][title]);
    }
  });
}

// Функция для обновления активного состояния кнопок языка
function updateActiveLanguageButton() {
  const langButtons = document.querySelectorAll('#ruen a');
  langButtons.forEach(button => {
    if (button.textContent === currentLanguage.toUpperCase()) {
      button.classList.add('active');
    } else {
      button.classList.remove('active');
    }
  });
}

// Инициализация локализации
document.addEventListener('DOMContentLoaded', () => {
  // Установка обработчиков событий для кнопок языка
  const langButtons = document.querySelectorAll('#ruen a');
  langButtons.forEach(button => {
    button.addEventListener('click', (e) => {
      e.preventDefault();
      const lang = button.textContent.toLowerCase();
      setLanguage(lang);
    });
  });

  // Инициализация текущего языка
  setLanguage(currentLanguage);
});

