async function importProjects(strapi) {
  for (const project of projects) {
    const existing = await strapi.entityService.findMany('api::article.article', {
      filters: { Title: { $eq: project.title } },
      limit: 1,
    });

    if (existing && existing.length > 0) {
      continue;
    }

    const authorIds = await findOrCreateAuthors(strapi, project.authors || []);
    const tagIds = await findOrCreateTags(strapi, project.tags || []);

    const solutionsText = project.solutions ? project.solutions.join('\n') : null;
    const purposesText = project.purposes ? project.purposes.join('\n') : null;
    await strapi.entityService.create('api::article.article', {
      data: {
        Title: project.title,
        Description: project.shortDescription || null,
        Content: buildContentBlocks(project),
        Solutions: solutionsText,
        Purposes: purposesText,
        Publication: project.publicationDate,
        Faculty: project.faculty,
        ScienceArea: project.scienceArea,
        ScienceDirection: project.scienceDirection,
        authors: authorIds,
        tags: tagIds,
      },
    });
  }
}

